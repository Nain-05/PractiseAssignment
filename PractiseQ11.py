Letter= input('\nEnter Letter: ')
if Letter == 'a' or Letter == 'e' or Letter == 'i' or Letter == 'o' or Letter == 'u' or Letter == 'A' or Letter == 'E' or Letter == 'I' or Letter == 'O' or Letter == 'U':
    print('\nLetter is Vowel\n')
else:
    print('\nLetter is not Vowel\n')